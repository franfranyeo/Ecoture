namespace EcotureAPI.Models.Enum
{
    /// <summary>
    /// Enum to represent various price ranges for products.
    /// </summary>
    public enum PriceRange
    {
        TenToTwenty = 1,        // $10 - $20
        TwentyToThirty = 2,     // $20 - $30
        ThirtyToForty = 3,      // $30 - $40
        FortyToFifty = 4,       // $40 - $50
        FiftyPlus = 5           // $50 and above
    }
}
